fun main()
{
    var user_val = 3

    if(user_val == 1)
    {
        println("Triangle Area")

        var radius = 10

        var myResult =  22 * radius * radius / 7


        println (myResult)

    }

    if(user_val == 2)
    {
        println("Circle Area")

        var width = 3
        var height = 6

        var myResult =  width * height / 2


        println (myResult)

    }

    if(user_val == 3)
    {
        println("Rectangle Area")

        var length = 3
        var width = 6

        var myResult =  length * width


        println (myResult)

    }

}